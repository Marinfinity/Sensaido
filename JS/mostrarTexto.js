function mostrarMasBio() {
    var infoLibro = document.getElementById("infoBio");
    var boton = document.getElementById("saberMasBioBtn");

    if (infoLibro.style.display === "none" || infoLibro.style.display === "") {
        infoLibro.style.display = "block";
        infoLibro.classList.remove("fade-out");
        void infoLibro.offsetWidth; // Reinicia animación
        infoLibro.classList.add("fade-in");
        boton.textContent = "Mostrar Menos";
    } else {
        infoLibro.classList.remove("fade-in");
        infoLibro.classList.add("fade-out");
        boton.textContent = "Mostrar Más";

        // Esperar a que termine la animación antes de ocultar
        setTimeout(function () {
            infoLibro.style.display = "none";
        }, 500); // Debe coincidir con la duración de fadeOut (0.5s)
    }
}

